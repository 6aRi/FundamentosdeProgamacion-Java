package Semana5.Calculadora;

/**
 *
 * @author gabri
 */
public class Calculadora {
    
    public double getSuma(double a, double b){
        return a+b;
    }
    
    public double getResta(double a, double b){
        return a-b;
    }
    
    public double getDivision(double a, double b){
        return a/b;
    }
    
}
